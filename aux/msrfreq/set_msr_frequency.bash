for a in 0x1a00 0x1900 0x1800 0x1700 0x1500 0x1400 0x1300 0x1200 0x1100 0x1000 0xf00 0xd00 0xc00 0xb00 0xa00 
do
    echo $a
    sudo wrmsr -a 0x199 $a
    sudo ./check_requested_and_set_freq.bash
    ./pseudo_urandom.bash 20 & # 10 seconds
    sleep 4
    cpufreq-info -c 0 | grep 'current CPU'
    sleep 2
done

